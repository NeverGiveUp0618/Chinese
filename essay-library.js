  function makeCloze(paras, fields) {
    return paras.map(p => {
      let out = p;
      fields.forEach(f => {
        const escaped = f.value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
        out = out.replace(new RegExp(escaped, "g"), `{{${f.key}}}`);
      });
      return out;
    });
  }

